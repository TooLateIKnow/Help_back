package com.help.pojo;

public class Helpinfo {
    private String userAFHId;
    private String userPHId;
    private String location;
    private String time;
    private String helpWhat;

    public String getUserAFHId() {
        return userAFHId;
    }

    public void setUserAFHId(String userAFHId) {
        this.userAFHId = userAFHId;
    }

    public String getUserPHId() {
        return userPHId;
    }

    public void setUserPHId(String userPHId) {
        this.userPHId = userPHId;
    }

    public String getLocation() {
        return location;
    }

    public void setLocation(String location) {
        this.location = location;
    }

    public String getTime() {
        return time;
    }

    public void setTime(String time) {
        this.time = time;
    }

    public String getHelpWhat() {
        return helpWhat;
    }

    public void setHelpWhat(String helpWhat) {
        this.helpWhat = helpWhat;
    }
}
